package com.app.incroyable.quickgrid.model;

public class Frame {

    int icon, image;

    public Frame(int icon, int image) {
        this.icon = icon;
        this.image = image;
    }

    public int getIcon() {
        return icon;
    }

    public void setIcon(int icon) {
        this.icon = icon;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }
}
